fn run_case(case: &GoldenCase) {
    let input_path = fixture_path(case.input);
    let cache_dir = fixture_path(case.cache_dir);
    let policy_path = references_path(case.policy);
    let patch_snapshot_path = fixture_path(case.expected_patch_plan);
    let hardened_snapshot_path = fixture_path(case.expected_hardened);
    let anchored_snapshot_path = fixture_path(case.expected_anchored);

    let input_content =
        fs::read_to_string(&input_path).expect("golden fixture input should be readable");
    let extracted = extract::extract_image_sets(&input_content)
        .expect("extract should parse deterministic image references");
    assert!(
        !extracted.valid.is_empty(),
        "fixture {} should contain image references",
        case.name
    );
    assert!(
        extracted.unresolved.is_empty(),
        "fixture {} should not contain unresolved image references",
        case.name
    );

    let cache_path = cache_dir.join("image-profiles.json");
    let cache_payload =
        cache::read_cache(&cache_path).expect("golden fixture cache should parse successfully");
    let policy_pack = policy::load_policy_pack(&policy_path, PolicyDomain::Compose)
        .expect("golden fixture policy should compile");
    for rule_id in case.expected_warn_rule_ids {
        let rule = policy_pack
            .rules
            .iter()
            .find(|rule| rule.id == *rule_id)
            .unwrap_or_else(|| panic!("fixture {} missing policy rule `{}`", case.name, rule_id));
        assert!(
            matches!(rule.severity, RuleSeverity::Warn),
            "fixture {} expected `{}` severity warn in policy pack",
            case.name,
            rule_id
        );
    }

    let initial = policy::evaluate_compose_policy_with_mode(
        &input_content,
        &cache_payload,
        &policy_pack,
        case.mode,
    )
    .expect("initial policy evaluation should succeed");

    for expected in case.expected_violation_substrings {
        assert!(
            initial
                .violations
                .iter()
                .any(|violation| violation.reason.contains(expected)),
            "fixture {} expected violation containing `{}`",
            case.name,
            expected
        );
    }
    let mut actual_violation_rule_ids: Vec<&str> = initial
        .violations
        .iter()
        .map(|item| item.rule_id.as_str())
        .collect();
    actual_violation_rule_ids.sort_unstable();
    let mut expected_violation_rule_ids: Vec<&str> = case.expected_violation_rule_ids.to_vec();
    expected_violation_rule_ids.sort_unstable();
    assert_eq!(
        actual_violation_rule_ids.len(),
        expected_violation_rule_ids.len(),
        "fixture {} violation count mismatch.\nexpected ids: {:?}\nactual ids: {:?}",
        case.name,
        expected_violation_rule_ids,
        actual_violation_rule_ids
    );
    assert!(
        actual_violation_rule_ids == expected_violation_rule_ids,
        "fixture {} violation rule ids drift.\nexpected: {:?}\nactual: {:?}",
        case.name,
        expected_violation_rule_ids,
        actual_violation_rule_ids
    );
    for op in case.required_patch_ops {
        assert!(
            initial.patch_plan.iter().any(|patch| patch.op == *op),
            "fixture {} expected patch op `{}`",
            case.name,
            op
        );
    }
    for op in case.forbidden_patch_ops {
        assert!(
            !initial.patch_plan.iter().any(|patch| patch.op == *op),
            "fixture {} should not include patch op `{}`",
            case.name,
            op
        );
    }

    let rendered_patch_plan =
        serde_json::to_string_pretty(&initial.patch_plan).expect("patch plan should serialize");
    let expected_patch_plan =
        fs::read_to_string(&patch_snapshot_path).expect("patch plan snapshot should be readable");
    assert!(
        !rendered_patch_plan.trim().is_empty(),
        "fixture {} patch plan must not be empty",
        case.name
    );
    assert_eq!(
        rendered_patch_plan.trim(),
        expected_patch_plan.trim(),
        "patch plan drift for fixture {}",
        case.name
    );

    let hardened = policy::apply_compose_patch_plan(&input_content, &initial.patch_plan, case.mode)
        .expect("compose patch application should succeed");
    let expected_hardened =
        fs::read_to_string(&hardened_snapshot_path).expect("hardened snapshot should be readable");
    let expected_hardened = strip_snapshot_header(&expected_hardened);
    assert!(
        !hardened.trim().is_empty(),
        "fixture {} hardened output must not be empty",
        case.name
    );
    assert_eq!(
        hardened.trim(),
        expected_hardened.trim(),
        "hardened compose drift for fixture {}",
        case.name
    );

    let reevaluated = policy::evaluate_compose_policy_with_mode(
        &hardened,
        &cache_payload,
        &policy_pack,
        case.mode,
    )
    .expect("policy re-evaluation should succeed");
    assert!(
        reevaluated.patch_plan.is_empty(),
        "fixture {} should be idempotent after first hardening apply",
        case.name
    );
    let mut remaining_violation_rule_ids: Vec<&str> = reevaluated
        .violations
        .iter()
        .map(|item| item.rule_id.as_str())
        .collect();
    remaining_violation_rule_ids.sort_unstable();
    let mut allowed_violation_rule_ids: Vec<&str> =
        case.allowed_remaining_violation_rule_ids.to_vec();
    allowed_violation_rule_ids.sort_unstable();
    assert_eq!(
        remaining_violation_rule_ids.len(),
        allowed_violation_rule_ids.len(),
        "fixture {} residual violation count mismatch after hardening.\nexpected ids: {:?}\nactual ids: {:?}",
        case.name,
        allowed_violation_rule_ids,
        remaining_violation_rule_ids
    );
    assert_eq!(
        remaining_violation_rule_ids, allowed_violation_rule_ids,
        "fixture {} residual violation drift after hardening",
        case.name
    );

    let anchored =
        generator::generate_anchored_compose(&hardened, case.mode, AnchorMode::Auto, None)
            .expect("compose anchor generation should succeed");
    let anchored_second =
        generator::generate_anchored_compose(&hardened, case.mode, AnchorMode::Auto, None)
            .expect("repeat anchor generation should succeed");
    assert_eq!(
        anchored, anchored_second,
        "anchor generation should be deterministic for fixture {}",
        case.name
    );

    let expected_anchored =
        fs::read_to_string(&anchored_snapshot_path).expect("anchored snapshot should be readable");
    let expected_anchored = strip_snapshot_header(&expected_anchored);
    assert!(
        !anchored.trim().is_empty(),
        "fixture {} anchored output must not be empty",
        case.name
    );
    assert_eq!(
        anchored.trim(),
        expected_anchored.trim(),
        "anchored compose drift for fixture {}",
        case.name
    );
}

fn run_dockerfile_case(case: &DockerfileGoldenCase) {
    let input_path = fixture_path(case.input);
    let policy_path = references_path(case.policy);
    let patch_snapshot_path = fixture_path(case.expected_patch_plan);
    let cache_payload = case.cache_dir.map(|cache_dir| {
        cache::read_cache(&fixture_path(cache_dir).join("image-profiles.json"))
            .expect("dockerfile fixture cache should parse successfully")
    });

    let input_content =
        fs::read_to_string(&input_path).expect("dockerfile golden fixture should be readable");
    let policy_pack = policy::load_policy_pack(&policy_path, PolicyDomain::Dockerfile)
        .expect("dockerfile golden policy should compile");

    let initial = policy::evaluate_dockerfile_policy_with_cache_and_source(
        &input_content,
        &policy_pack,
        cache_payload.as_ref(),
        Some(&input_path),
    )
    .expect("dockerfile policy evaluation should succeed");

    for rule_id in case.required_violation_rule_ids {
        assert!(
            initial
                .violations
                .iter()
                .any(|violation| violation.rule_id == *rule_id),
            "fixture {} expected violation with rule id `{}`",
            case.name,
            rule_id
        );
    }
    for rule_id in case.forbidden_patch_rule_ids {
        assert!(
            !initial
                .patch_plan
                .iter()
                .any(|patch| patch.rule_id == *rule_id),
            "fixture {} should not auto-patch detect-only rule id `{}`",
            case.name,
            rule_id
        );
    }

    let rendered_patch_plan =
        serde_json::to_string_pretty(&initial.patch_plan).expect("patch plan should serialize");
    let expected_patch_plan =
        fs::read_to_string(&patch_snapshot_path).expect("patch plan snapshot should be readable");
    assert!(
        !rendered_patch_plan.trim().is_empty(),
        "fixture {} dockerfile patch plan must not be empty",
        case.name
    );
    assert_eq!(
        rendered_patch_plan.trim(),
        expected_patch_plan.trim(),
        "dockerfile patch plan drift for fixture {}",
        case.name
    );

    let hardened = policy::apply_dockerfile_patch_plan(&input_content, &initial.patch_plan)
        .expect("dockerfile patch application should succeed");
    let expected_hardened = fs::read_to_string(fixture_path(case.expected_hardened))
        .expect("hardened snapshot readable");
    let expected_hardened = strip_snapshot_header(&expected_hardened);
    assert!(
        !hardened.trim().is_empty(),
        "fixture {} dockerfile hardened output must not be empty",
        case.name
    );
    assert_eq!(
        hardened.trim(),
        expected_hardened.trim(),
        "dockerfile hardened drift for fixture {}",
        case.name
    );
    let reevaluated = policy::evaluate_dockerfile_policy_with_cache_and_source(
        &hardened,
        &policy_pack,
        cache_payload.as_ref(),
        Some(&input_path),
    )
    .expect("dockerfile policy re-evaluation should succeed");
    let mut remaining_rule_ids: Vec<&str> = reevaluated
        .patch_plan
        .iter()
        .map(|patch| patch.rule_id.as_str())
        .collect();
    remaining_rule_ids.sort_unstable();
    let mut allowed_rule_ids: Vec<&str> = case.allowed_remaining_patch_rule_ids.to_vec();
    allowed_rule_ids.sort_unstable();
    assert_eq!(
        remaining_rule_ids.len(),
        allowed_rule_ids.len(),
        "dockerfile residual patch count mismatch for fixture {}.\nexpected ids: {:?}\nactual ids: {:?}",
        case.name,
        allowed_rule_ids,
        remaining_rule_ids
    );
    assert_eq!(
        remaining_rule_ids, allowed_rule_ids,
        "dockerfile residual patch drift for fixture {}",
        case.name
    );
    let mut remaining_violation_rule_ids: Vec<&str> = reevaluated
        .violations
        .iter()
        .map(|item| item.rule_id.as_str())
        .collect();
    remaining_violation_rule_ids.sort_unstable();
    let mut allowed_violation_rule_ids: Vec<&str> =
        case.allowed_remaining_violation_rule_ids.to_vec();
    allowed_violation_rule_ids.sort_unstable();
    assert_eq!(
        remaining_violation_rule_ids.len(),
        allowed_violation_rule_ids.len(),
        "dockerfile residual violation count mismatch for fixture {}.\nexpected ids: {:?}\nactual ids: {:?}",
        case.name,
        allowed_violation_rule_ids,
        remaining_violation_rule_ids
    );
    assert_eq!(
        remaining_violation_rule_ids, allowed_violation_rule_ids,
        "dockerfile residual violation drift for fixture {}",
        case.name
    );
}

