fn panic_payload_to_string(payload: Box<dyn std::any::Any + Send>) -> String {
    if let Some(message) = payload.downcast_ref::<&str>() {
        return (*message).to_string();
    }
    if let Some(message) = payload.downcast_ref::<String>() {
        return message.clone();
    }
    "non-string panic payload".to_string()
}

fn strip_snapshot_header(snapshot: &str) -> &str {
    if let Some(stripped) = snapshot.strip_prefix(SNAPSHOT_HEADER) {
        if let Some(without_newline) = stripped.strip_prefix('\n') {
            return without_newline;
        }
        if let Some(without_newline) = stripped.strip_prefix("\r\n") {
            return without_newline;
        }
        return stripped;
    }
    snapshot
}

fn assert_snapshot_header(path: &Path) {
    let content = fs::read_to_string(path).unwrap_or_else(|error| {
        panic!("snapshot {} should be readable: {error}", path.display());
    });
    assert!(
        content.starts_with(SNAPSHOT_HEADER),
        "snapshot {} is missing auto-generated header",
        path.display()
    );
}

