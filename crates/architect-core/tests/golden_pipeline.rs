#[test]
fn compose_and_swarm_golden_pipeline_remains_deterministic() {
    let mut failures = Vec::new();
    for case in golden_cases() {
        if let Err(payload) = catch_unwind(AssertUnwindSafe(|| run_case(&case))) {
            failures.push(format!(
                "{}: {}",
                case.name,
                panic_payload_to_string(payload)
            ));
        }
    }
    assert!(
        failures.is_empty(),
        "compose/swarm golden failures:\n\n{}",
        failures.join("\n\n")
    );
}

#[test]
fn dockerfile_golden_pipeline_remains_deterministic() {
    let mut failures = Vec::new();
    for case in dockerfile_golden_cases() {
        if let Err(payload) = catch_unwind(AssertUnwindSafe(|| run_dockerfile_case(&case))) {
            failures.push(format!(
                "{}: {}",
                case.name,
                panic_payload_to_string(payload)
            ));
        }
    }
    assert!(
        failures.is_empty(),
        "dockerfile golden failures:\n\n{}",
        failures.join("\n\n")
    );
}

#[test]
fn compose_init_perms_hardened_fixture_is_idempotent_under_full_enforcing_policy() {
    let hardened_path =
        fixture_path("tests/golden/compose_init_perms_named_volume/expected.hardened.yaml");
    let cache_path =
        fixture_path("tests/golden/compose_init_perms_named_volume/cache/image-profiles.json");
    let policy_path = references_path("policy-compose-enforcing.yaml");

    let hardened = fs::read_to_string(&hardened_path).expect("hardened fixture should be readable");
    let cache_payload =
        cache::read_cache(&cache_path).expect("golden fixture cache should parse successfully");
    let policy_pack = policy::load_policy_pack(&policy_path, PolicyDomain::Compose)
        .expect("compose enforcing policy should compile");

    let reevaluated = policy::evaluate_compose_policy_with_mode(
        &hardened,
        &cache_payload,
        &policy_pack,
        DeployMode::Compose,
    )
    .expect("policy re-evaluation should succeed");
    assert!(
        reevaluated.violations.is_empty(),
        "hardened init-perms fixture should produce no violations under full enforcing policy"
    );
    assert!(
        reevaluated.patch_plan.is_empty(),
        "hardened init-perms fixture should produce no additional patches under full enforcing policy"
    );
}

